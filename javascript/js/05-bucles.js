'use strict';

/*======================================================================================================================
                                            Bucle For
=======================================================================================================================*/

for(let i = 0; i <= 10; i++){
    console.log("for:", i);
}

/*======================================================================================================================
                                            Bucle While (mientras)
=======================================================================================================================*/

let n = 1;
while(n <= 5){
    console.log("while:",n);
    n++;
}


/*======================================================================================================================
                                            Bucle Do While
=======================================================================================================================*/
let p = 1;

do{
    console.log("do while:", p);
    p++;
}
while(p <= 5);

